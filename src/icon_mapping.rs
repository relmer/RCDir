    //
    //  test_all_glyphs_are_valid_unicode
    //
    //  All icon glyphs must be valid NF Private Use Area code points.
    //
    ////////////////////////////////////////////////////////////////////////////

    #[test]
    fn test_all_glyphs_are_valid_unicode() {
        let is_nf_range = |c: char| -> bool {
            let cp = c as u32;
            // Nerd Font PUA ranges: BMP PUA (E000–F8FF) and Supplementary PUA (F0000–FFFFF)
            (0xE000..=0xF8FF).contains (&cp) || (0xF0000..=0xFFFFF).contains (&cp)
        };

        for &(key, glyph) in DEFAULT_EXTENSION_ICONS {
            assert! (is_nf_range (glyph),
                "Extension '{}' has glyph U+{:04X} outside NF PUA range", key, glyph as u32);
        }

        for &(key, glyph) in DEFAULT_WELL_KNOWN_DIR_ICONS {
            assert! (is_nf_range (glyph),
                "Dir '{}' has glyph U+{:04X} outside NF PUA range", key, glyph as u32);
        }
    }
